const MORSE_TABLE = {
    '.-':     'a',
    '-...':   'b',
    '-.-.':   'c',
    '-..':    'd',
    '.':      'e',
    '..-.':   'f',
    '--.':    'g',
    '....':   'h',
    '..':     'i',
    '.---':   'j',
    '-.-':    'k',
    '.-..':   'l',
    '--':     'm',
    '-.':     'n',
    '---':    'o',
    '.--.':   'p',
    '--.-':   'q',
    '.-.':    'r',
    '...':    's',
    '-':      't',
    '..-':    'u',
    '...-':   'v',
    '.--':    'w',
    '-..-':   'x',
    '-.--':   'y',
    '--..':   'z',
    '.----':  '1',
    '..---':  '2',
    '...--':  '3',
    '....-':  '4',
    '.....':  '5',
    '-....':  '6',
    '--...':  '7',
    '---..':  '8',
    '----.':  '9',
    '-----':  '0',
};
MORSE_TABLE['**********'] = ' ';

function decode( expr ) {
    let symbols = expr.match( /.{10}/g );
    let symbol = '';
    let string = symbols.reduce( ( sum, currentSymbol ) => {
        symbol = currentSymbol.replace( /00/g, '' );
        symbol = symbol.replace( /10/g, '.' ).replace( /11/g, '-' );
        return sum = sum + MORSE_TABLE[ symbol ];
    }, '' );
    return string;
}

module.exports = {
    decode
}